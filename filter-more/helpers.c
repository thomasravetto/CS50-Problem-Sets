// Convert image to grayscale
void grayscale(int height, int width, RGBTRIPLE image[height][width])
{
    for (int i = 0; i < height; i++)
    {
        for (int j = 0; j < width; j++)
        {
            // Creating a value using the average color of the pixel
            int greyscale = round((image[i][j].rgbtRed + image[i][j].rgbtGreen + image[i][j].rgbtBlue) / 3.0);

            // Updating the color values to be the greyscale ones
            image[i][j].rgbtRed = image[i][j].rgbtGreen = image[i][j].rgbtBlue = greyscale;
        }
    }
    return;
}

